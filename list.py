def squareList(begValue, endValue):

    lst = []
    oddLst = []
    evenLst = []
    lstValue = 0

    for i in range(begValue, endValue + 1):
        lstValue = i
        square = lstValue**2

        lst.append(square)

    for i in lst:
        if i%2 == 0:
            evenLst.append(i)
            return oddLst
        else:
            oddLst.append(i)
            return evenLst
        

    return lst

